/** Pick a contextual reply based on user message. Falls back to random if no match. */
export function getContextualBotReply(userMessage: string): string {
  const lower = userMessage.toLowerCase().trim();
  if (!lower) return getRandomBotReply();
  for (const { keywords, replies } of KEYWORD_MAP) {
    if (keywords.some((k) => lower.includes(k))) {
      const idx = Math.floor(Math.random() * replies.length);
      return replies[idx];
    }
  }
  return getRandomBotReply();
}

export function getRandomBotReply(): string {
  const idx = Math.floor(Math.random() * DUEL_BOT_REPLIES.length);
  return DUEL_BOT_REPLIES[idx];
}

export function getBotName(seed?: string): string {
  const idx = seed
    ? seed.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0) % DUEL_BOT_NAMES.length
    : Math.floor(Math.random() * DUEL_BOT_NAMES.length);
  return DUEL_BOT_NAMES[idx];
}
